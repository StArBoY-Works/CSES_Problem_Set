void concert_tickets()
{
    int size_1,size_2;
    cin>>size_1>>size_2;
    multiset<int> ticket;
    for(int i = 0; i < size_1; i++)
    {
        int temp;
        cin>>temp;
        ticket.insert(temp);
    }
    
    for(int i = 0; i < size_2; i++)
    {
        int temp;
        cin>>temp;
        auto ptr = ticket.upper_bound(temp);
        if(ptr == ticket.begin())
        {
            cout<<-1<<endl;
        }
        else
        {
            cout<<*(--ptr)<<endl;
            ticket.erase(ptr);
        }
    }
}